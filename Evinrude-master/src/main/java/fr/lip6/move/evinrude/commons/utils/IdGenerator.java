package fr.lip6.move.evinrude.commons.utils;

/**
 * Classe permettant de générer des identifiants de type numérique
 * @author Clément Démoulins
 */
public class IdGenerator {
	private int id = 0;

	/**
	 * @return un identifiant unique pour cette instance
	 */
	public final int next() {
		return id++;
	}
}
