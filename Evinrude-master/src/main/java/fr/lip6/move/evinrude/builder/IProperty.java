package fr.lip6.move.evinrude.builder;

import fr.lip6.move.evinrude.commons.model.petri.interfaces.ISubModel;

/**
 * Interface d'une propriété. Les propriétés permettent de faire de la vérification de réseau de petri.
 */
public interface IProperty {
	/**
	 * Vérification de la propriété.
	 * @param subModel sous modèle à vérifier
	 * @return <code>true</code> si la propriété est vérifié, <code>false</code> sinon.
	 */
	boolean check(ISubModel subModel);

	/**
	 * @return Property name
	 */
	String getName();
}
