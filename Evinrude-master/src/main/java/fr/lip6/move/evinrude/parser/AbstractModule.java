package fr.lip6.move.evinrude.parser;

/**
 * Implémentation de base d'un module du parser.
 */
public abstract class AbstractModule implements IModule {

	private IParser parser;

	/**
	 * Constructeur
	 * @param parser Objet parser permettant de récupérer certains paramètres courrant
	 */
	public AbstractModule(IParser parser) {
		this.parser = parser;
	}

	/**
	 * @return the parser
	 */
	protected final IParser getParser() {
		return parser;
	}

	/**
	 * @param parser the parser to set
	 */
	protected final void setParser(IParser parser) {
		this.parser = parser;
	}

}
